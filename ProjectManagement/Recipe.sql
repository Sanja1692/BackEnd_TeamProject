Camille Bergerson

Ingredients


50 g powdered sugar
120 g creamy peanut butter
170 g chocolate, melted

Description:
Prepare a cupcake tin with 6 liners. Stir peanut butter and powdered sugar together until smooth. Spread 1 to 2 tablespoons of chocolate in the bottom of each cupcake liner. Dollop 1 to 2 teaspoons of the peanut butter mixture on top of the chocolate.
Cover each dollop of peanut butter with more chocolate and smooth out the top. Refrigerate for 1 hour or until chocolate has hardened.
Remove peanut butter cups from the liners. 

Preparation Steps:
1. Prepare a cupcake tin with 6 liners.
2. Stir peanut butter and powdered sugar together until smooth.
3. Spread 1 to 2 tablespoons of chocolate in the bottom of each cupcake liner.
4. Dollop 1 to 2 teaspoons of the peanut butter mixture on top of the chocolate.
5. Cover each dollop of peanut butter with more chocolate and smooth out the top.
6. Refrigerate for 1 hour or until chocolate has hardened.
7. Remove peanut butter cups from the liners.
Enjoy!

